'use strict'

const fs = require('fs')
const path = require('path')

const packageName = process.env.APP_PACKAGE_NAME || 'com.lxwalnut.music.mobile'
const deepLinkScheme = process.env.APP_DEEP_LINK_SCHEME || 'lxmusic'

if (!/^[A-Za-z][A-Za-z0-9_]*(\.[A-Za-z][A-Za-z0-9_]*)+$/.test(packageName)) {
  throw new Error(`Invalid APP_PACKAGE_NAME: ${packageName}`)
}
if (!/^[A-Za-z][A-Za-z0-9+.-]*$/.test(deepLinkScheme)) {
  throw new Error(`Invalid APP_DEEP_LINK_SCHEME: ${deepLinkScheme}`)
}

const identity = {
  packageName,
  providerAuthority: `${packageName}.provider`,
  widgetActionPrefix: `${packageName}.widget`,
  deepLinkScheme,
}

const output = path.resolve(__dirname, '../src/config/buildIdentity.ts')
fs.writeFileSync(output, `// Generated by scripts/generate-build-identity.js\nexport const BUILD_IDENTITY = ${JSON.stringify(identity, null, 2)} as const\n`)
console.log(`Generated ${path.relative(process.cwd(), output)} for ${packageName}`)
